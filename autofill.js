document.querySelectorAll('div.qtext').forEach((questionElement) => {
    const question = questionElement.innerText.trim();
    const correctAnswer = questionAnswers[question];

    if (correctAnswer) {
        const answersDiv = questionElement.closest('.qtext').nextElementSibling;
        const optionLabels = answersDiv.querySelectorAll('label');

        let fillin = false;
        optionLabels.forEach((label) => {
            const option = label.innerText.replace(/^[A-Ea-e]\. /, '').trim();
            if (option == correctAnswer) {
                const input = label.previousElementSibling;
                if (input && input.type === 'radio') {
                    input.click();
                    console.log(`Fill: ${correctAnswer}`);
                    fillin = true;
                }
            }
        });
        if(!fillin) {
            console.warn(`Cannot find the corresponding answer in the options: ${correctAnswer}`);
        }
    } else {
        console.warn(`Cannot find the answer: ${question}`);
    }
});
